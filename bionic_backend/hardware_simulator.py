"""
Hardware Simulator - Sends real-time sensor data to backend
============================================================
This script simulates an ESP32/Arduino device by sending realistic
sensor data to the Django backend every 100ms (just like real hardware).

Use this to test real-time readings WITHOUT physical hardware!

Requirements:
- Backend server running on http://127.0.0.1:8000
- Device token from database (generated by seed_data.py)

Usage:
python hardware_simulator.py
"""

import requests
import time
import random
import math
from datetime import datetime

# ─────────────────────────────────────────────────────────────
# CONFIGURATION
# ─────────────────────────────────────────────────────────────
SERVER_URL = "http://127.0.0.1:8000/api/v1"
DEVICE_TOKEN = ""  # Will be auto-filled from database if empty

# Simulation settings
SEND_INTERVAL_SEC = 0.1  # 100ms (same as Arduino firmware)
SIMULATE_GESTURES = True  # Simulate different gestures automatically

# Gesture patterns (EMG value ranges)
GESTURE_PATTERNS = {
    'IDLE':   {'emg_base': 40, 'emg_var': 20, 'force': 0.1},
    'OPEN':   {'emg_base': 200, 'emg_var': 50, 'force': 0.2},
    'GRIP':   {'emg_base': 650, 'emg_var': 100, 'force': 5.5},
    'PINCH':  {'emg_base': 380, 'emg_var': 60, 'force': 2.5},
    'ROTATE': {'emg_base': 150, 'emg_var': 40, 'force': 1.0},
}


